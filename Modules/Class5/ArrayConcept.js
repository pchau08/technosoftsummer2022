 console.log(`\nplanets -> ${planets}`);
 console.log(`typeof planets -> ${typeof planets}`);
 
 /**
  * To find the number of values in array
  * property: length
  */
 const totalPlanets = planets.length;
 console.log(totalPlanets);
 
 
